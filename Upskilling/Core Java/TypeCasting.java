public class TypeCasting {
    public static void main(String[] args){
        double myDouble = 9.56;
        int myval=(int)myDouble;
        System.out.println("Double value typecasted to int: "+myval);

        int myInt = 55;
        double myDouble2 = myInt;
        System.out.println("Integer value typecasted to double: "+myDouble2);
    }
}
